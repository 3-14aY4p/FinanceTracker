    private void btn_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_confirmActionPerformed
        if (list_source.isSelectionEmpty()|| list_destination.isSelectionEmpty()|| tf_amount.getText().isEmpty() || dc_date.getDate() == null) {
            JOptionPane.showMessageDialog(null, "[ERR] Empty fields found.");
            return;
        }
        if (list_source.getSelectedValue().equals(list_destination.getSelectedValue())) {
            JOptionPane.showMessageDialog(null, "[ERR] Source and Destination cannot be the same.");
            return;
        }
        
        try {
            int t_id = selectedTransaction.getTransactionID();
            Date date = dc_date.getDate();
            double amnt = Double.parseDouble(tf_amount.getText());
            String desc = tf_description.getText();

            Account accSrc = null;
            int accIndSrc = 0;
            for (Account a : accArray) {
                if (a.getAccountName().equals(list_source.getSelectedValue())) {
                    accSrc = a;
                    accIndSrc = accArray.indexOf(a);
                    break;
                }
            }
            Account accDst = null;
            int accIndDst = 0;
            for (Account a : accArray) {
                if (a.getAccountName().equals(list_destination.getSelectedValue())) {
                    accDst = a;
                    accIndDst = accArray.indexOf(a);
                    break;
                }
            }
            
            if (accSrc == null || accDst == null) {
                JOptionPane.showMessageDialog(null, "[ERR] Could not find selected account.");
                return;
            }

            selectedTransaction.undoTransaction();
            for (Account a : accArray) {
                if (a.getAccountName().equals(selectedTransaction.getAccount().getFirst().getAccountName())) {
                    a.deposit(selectedTransaction.getAmount());
                }
                else if (a.getAccountName().equals(selectedTransaction.getAccount().getLast().getAccountName())) {
                    a.withdraw(selectedTransaction.getAmount());
                }
            }

            if (amnt <= 0) {
                JOptionPane.showMessageDialog(null,"[ERR] Invalid amount.");
                return;
            }

            if (amnt > accSrc.getBalance()) {
                JOptionPane.showMessageDialog(null, "[ERR] Insufficient balance.");
                return;
            }

            Transaction newTransaction = new TransferTransaction(t_id, date, amnt, desc, accSrc, accDst);
            newTransaction.confirmTransaction();

            for (int i = 0; i < trnsArray.size(); i++) {

                if (trnsArray.get(i).getTransactionID() == t_id) {
                    trnsArray.set(i, newTransaction);
                    break;
                }
            }

            JOptionPane.showMessageDialog(null, "Transaction Revised.");
            saveAccountsToFile();
            saveTransactionsToFile();
            
            parent.dispose();
            new ManageTransactionsPage().setVisible(true);
            
            this.dispose();
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "[ERR] Invalid amount.");
        }
    }//GEN-LAST:event_btn_confirmActionPerformed

