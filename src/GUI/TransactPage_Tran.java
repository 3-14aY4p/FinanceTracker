    private void btn_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_confirmActionPerformed
        if (list_source.isSelectionEmpty()|| list_destination.isSelectionEmpty()|| tf_amount.getText().isEmpty() || dc_date.getDate() == null) {
            JOptionPane.showMessageDialog(null, "[ERR] Empty fields found.");
            return;
        }
        
        if (list_source.getSelectedValue().equals(list_destination.getSelectedValue())) {
            JOptionPane.showMessageDialog(null, "[ERR] Can't have the same source and destination account.");
            return;
        }
        
        try {
            int t_id = transactions.size();
            for (Transaction t : transactions) {
                if (t.getTransactionID() > t_id) {
                    t_id = t.getTransactionID();
                }
            }
            
            Date date = dc_date.getDate();
            double amnt = Double.parseDouble(tf_amount.getText());
            String desc = tf_description.getText();

            Account accSrc = null;
            Account accDst = null;
            int accIndexSrc = 0;
            int accIndexDst = 0;
            
            for (Account a : accounts) {
                if (a.getAccountName().equals(list_source.getSelectedValue())) {
                    accSrc = a;
                    accIndexSrc = accounts.indexOf(a);
                }
                if (a.getAccountName().equals(list_destination.getSelectedValue())) {
                    accDst = a;
                    accIndexDst = accounts.indexOf(a);
                }
            }

            if (amnt > accSrc.getBalance()) {
                JOptionPane.showMessageDialog(null, "[ERR] Invalid amount. Please recheck your balance.");
                return;
            }
            else if (amnt <= 0) {
                JOptionPane.showMessageDialog(null, "[ERR] Invalid amount. Can't deposit negative number.");
                return;
            }
            
            currentTransaction = new TransferTransaction(t_id, date, amnt, desc, accSrc, accDst);

            currentTransaction.confirmTransaction();
            transactions.add(currentTransaction);
            
            accSrc = currentTransaction.getAccount().getFirst();
            accDst = currentTransaction.getAccount().getLast();
            accounts.set(accIndexSrc, accSrc);
            accounts.set(accIndexDst, accDst);

            JOptionPane.showMessageDialog(null, "Transaction Added.");
            saveAccountsToFile();
            saveTransactionsToFile();
            
            clearFields();
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "[ERR] Cannot parse balance. Please enter a valid number.");
        }
    }//GEN-LAST:event_btn_confirmActionPerformed
    
    
    public void clearFields() {
        list_source.setSelectedIndex(-1);
        list_destination.setSelectedIndex(-1);
        tf_amount.setText("");
        cmb_type.setSelectedIndex(0);
        transactionType = "expense";
        tf_description.setText("");
        dc_date.setDate(null);
    }
    
    public void saveAccountsToFile() {
        try {
            FileOutputStream file = new FileOutputStream("Accounts.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            for (int i = 0; i < accounts.size(); i++) {
                outputFile.writeObject(accounts.get(i));
            }
            outputFile.close();
        } 
        catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            
        }
    }
    public void saveTransactionsToFile() {
        try {
            FileOutputStream file = new FileOutputStream("Transactions.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            for (int i = 0; i < transactions.size(); i++) {
                outputFile.writeObject(transactions.get(i));
            }
            outputFile.close();
        } 
        catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            
        }
    }
    public void populateArrayList() {
        try {
            FileInputStream file = new FileInputStream("Accounts.dat");
            ObjectInputStream inputFile = new ObjectInputStream(file);
            
            boolean endOfFile = false;
            
            while (!endOfFile) {                
                try {
                    accounts.add((Account) inputFile.readObject());
                } 
                catch (EOFException e) {
                    endOfFile = true;
                }
                catch (IOException | ClassNotFoundException f) {
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            inputFile.close();
        } 
        catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        try {
            FileInputStream file2 = new FileInputStream("Transactions.dat");
            ObjectInputStream inputFile2 = new ObjectInputStream(file2);
            
            boolean endOfFile = false;
            
            while (!endOfFile) {                
                try {
                    transactions.add((Transaction) inputFile2.readObject());
                } 
                catch (EOFException e) {
                    endOfFile = true;
                }
                catch (IOException | ClassNotFoundException f) {
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            inputFile2.close();
        } 
        catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
